// Vakores Sendo Somabdo Por Eles Mesmo

let Valores=[100,200,300,400,500,600,700,800,900,1000]

let Soma_1=Valores.map((Numeros)=> Numeros + Numeros )

console.log(Soma_1)

// Valores Dividido Por 50 

let Soma_2=Valores.map((Numeros)=> Numeros/50)

console.log(Soma_2)

// Raiz Quadradas Do Valores 

let Soma_3=Valores.map((Numeros)=> Math.sqrt(Numeros))

console.log(Soma_3)