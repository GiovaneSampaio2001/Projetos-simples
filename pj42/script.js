function Ligou(){
let RESULTADO=document.querySelector(".Container")
RESULTADO.style.display="flex"
RESULTADO.style.flexDirection="row"
RESULTADO.style.justifyContent="flex-end"
RESULTADO.style.background="green"
document.body.style.background="green"
}


function Desligou(){
let RESU=document.querySelector(".Container")
RESU.style.display="flex"
RESU.style.flexDirection="row"
RESU.style.justifyContent="flex-start"
RESU.style.background="white"
document.body.style.background="black"

}


