function INGLATERRA(ENG){
  return `Bem Vindo A Inglaterra ${ENG} `
}


function VisitouInglaterra(){
let Inglaterra=document.querySelector("#Ting")
let Saida1=document.querySelector(".Resu1")
if(Inglaterra.value.length===0){
  window.alert("ERRO:Informe Seu Nome !")
}
else{
let ENG=(Inglaterra.value)
Saida1.innerHTML+=` ${INGLATERRA(ENG)} <br>`
}
}

